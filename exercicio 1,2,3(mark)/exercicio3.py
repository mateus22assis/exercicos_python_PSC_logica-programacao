#Faça um programa que receba a cotação do dólar em reais, e um valor que o usuário possui em dólares. Imprima este valor em reais.
print("Calculadora de conversão de dólares para reais") 

cotacao_dolar = float(input("Digite a cotação do dólar em reais: "))
valor_dolar = float(input("Digite o valor que você possui em dólares: "))

valor_reais = cotacao_dolar * valor_dolar
print("O valor em reais é: ", valor_reais)      